using System;

namespace MyMath
{
    /// <summary> Class to define Math operations. </summary>
    public class Operations 
    {
        /// <summary> Represent the sum of two integers</summary>
        /// <param name="a">#1 integer</param>
        /// <param name="b">#2 integer</param>
        /// <returns> Returns the addition of two integers</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
